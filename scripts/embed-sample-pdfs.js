const fs = require('fs');
const path = require('path');

const assetsDir = path.join(__dirname, '..', 'assets');
const constantsDir = path.join(__dirname, '..', 'src', 'constants');

if (!fs.existsSync(constantsDir)) {
  fs.mkdirSync(constantsDir, { recursive: true });
}

const egPath = path.join(assetsDir, 'erdgeschoss_muster.pdf');
const ogPath = path.join(assetsDir, 'obergeschoss_muster.pdf');

if (!fs.existsSync(egPath) || !fs.existsSync(ogPath)) {
  console.error('Muster PDFs not found. Please run generate-sample-pdfs.js first.');
  process.exit(1);
}

const egBase64 = fs.readFileSync(egPath).toString('base64');
const ogBase64 = fs.readFileSync(ogPath).toString('base64');

const tsContent = `// This file is auto-generated by scripts/embed-sample-pdfs.js. Do not edit manually.
export const ERDGESCHOSS_MUSTER_BASE64 = 'data:application/pdf;base64,${egBase64}';
export const OBERGESCHOSS_MUSTER_BASE64 = 'data:application/pdf;base64,${ogBase64}';
`;

fs.writeFileSync(path.join(constantsDir, 'samplePdfs.ts'), tsContent);
console.log('Successfully generated src/constants/samplePdfs.ts');
